public class Point {

    // coordinates X and Y
    private int x;
    private int y;

    //constructor
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // getter functions
    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
